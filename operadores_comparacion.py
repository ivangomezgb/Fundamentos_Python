#Operadores de comparacion

# Si es igual que / Comparacion ==
print(1==3) # 1 es igual a 1 = False u True

# Si es diferente / Diferente !=
print(2!=2) # 2 es diferente que 1 = False u True

#Si es mayor / Mayor que >
print(2>5) # 2 es mayor que 5 = False u True

#Si es menor / Menor que <
print(3<2)# 3 es menor que 2 = False u True

#Si es mayor o igual >=
print(3>=5) # 3 es mayor o igual que 5  = False u True

#Si es menor o igual <=
print(2<=1) # 2 es menor o igual que 3 = False u True

