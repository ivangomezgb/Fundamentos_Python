#Operadores logicos 

#AND
print(True and True) #Si ambos son verdaderos entonces

print(True and False)#Si uno es falso entonces 

print(False and True)#Si uno es falso entonces

print(False and False)#Si ambos son falsos entonces 

#OR

print(True or True) #Si ambos son verdaderos entonces

print(True or False)#Si uno es verdadero entonces 

print(False or True)#Si uno es verdadero entonces

print(False or False)#Si ambos son falsos entonces 

#NOT

print(not True) #Si es verdadero entonces

print(not False)#Si es falso entonces 

#Ejercicio AND

